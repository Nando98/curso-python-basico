#   Integer 10
#   Float   9.4
#   Boolean true - false
#   Complex ->  Machine learning application

number_one = 10
number_two = 4

result = number_one + number_two
print("La suma es :", result)

result = number_one - number_two
print("La resta es :", result)

result = number_one * number_two
print("El producto es :", result)

result = number_one / number_two
print("La división es :", result)

# Si se desea obtener un valor entero dividiendo, se coloca //
result = number_one // number_two
print("La división es :", result)

# Si se desea elevar a la potencia un número, se coloca **
result = number_one ** number_two
print("La división es :", result)

